void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
	uint_fast8_t var;
	if(complete+2<NUMBERLEDS)
	{
		++complete;
	}
	else if(complete<NUMBERLEDS)
	{
		++complete;
		for (var = 0; var < 8; ++var) {
			buffer[var+24]=0;
			buffer[var+32]=0;
			buffer[var+40]=0;
		}
	}
	else
	{
		modeled=HALT;
		HAL_TIM_PWM_Stop_DMA(&htim8, TIM_CHANNEL_1);
	}
}

